package variables.primitivos;

public class tipoInteger {
    public static void main(String[] args) {
        
        /*
            Las variables de tipo entero pueden almacener datos desde:
            -2147483648
            2147483647
        */
        
        int minEntero = Integer.MIN_VALUE;
        int maxEntero = Integer.MAX_VALUE;
        
        System.out.println("minEntero = " + minEntero);
        System.out.println("maxEntero = " + maxEntero);
        
    }
}
